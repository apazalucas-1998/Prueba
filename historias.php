     <h3 style="margin-top: 30px;">Origenes de la bicicletas</h3>
     <p style="line-height: 25px;">las bicicletas eléctricas  tuvieron su origen prácticamente al mismo tiempo en que fueron creadas las primeras bicicletas convencionales ya que en el año 1890 fueron otorgadas algunas patentes para motores que serían usados en bicicletas eléctricas.</p>
     <p style="line-height: 25px;">En Estados Unidos en el año 1895, le fue otorgada a Ogden Bolton la patente para una bicicleta con baterías la cual incluiría un colector de corriente continua, seis polos de cepillo y un motor de cubo ubicado encima de su rueda trasera.</p>
     <p style="line-height: 25px;">Por su parte y en el año 1897 en Boston, Óseas W. Libbey logró inventar una bicicleta eléctrica la cual se caracterizaba por ser impulsada a través de un motor eléctrico doble, que se encontraba sobre el centro del plato del eje.</p>
     <p style="line-height: 25px;">Tiempo después y en la década de los 90, el diseño de Libbey fue rehusado por la marca Giant Lafree e-bikes.</p>

