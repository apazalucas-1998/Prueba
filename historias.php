        <b>Argentina </b>
        <p style="line-height: 25px;">En el paseo del bosque de la ciudad de La Plata ya rueda la primera bicicleta eléctrica de Latinoamérica (en 2009). Turistas o vecinos que visitan el paseo del bosque de la ciudad de La Plata, provincia de Buenos Aires, Argentina, pueden conducir en forma gratuita las primeras bicicletas eléctricas de Latino-américa.</p>
        <p style="line-height: 25px;">La Municipalidad de La Plata pone estos vehículos a disposición de la gente que quiera conducirlos, los fines de semana, en el circuito seguro del bosque, para lo cual se debe presentar el documento de identidad y ser mayor de 18 años. Además a los ciclistas reciben los elementos de seguridad para circular. La finalidad de esta iniciativa es dar a conocer el rodado para impulsar en un futuro su incorporación en la ciudad como medio de transporte no contaminante y económico.</p>
