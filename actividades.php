        <h2>Bicicleta eléctrica v.s Bicicleta tradicional</h2>
        <br>
        

        <p style="line-height: 25px;">La idea de hacer este estudio surgió de 3 profesores ciclistas que, tras el auge de las bicicletas eléctricas, quisieron averiguar si su uso suponía el mismo nivel de exigencia física que una bici normal.</p>
        <p style="line-height: 25px;">En total participaron 33 ciclistas aficionados a los que se les puso a rodar por un circuito de montaña de casi 10 km. Ataviados con un sensor de frecuencia cardíaca, primero hicieron el circuito con una e-MTB y después con una bicicleta de montaña convencional.</p>
        <p style="line-height: 25px;">Los resultados arrojaron que los participantes solo tuvieron 9,9 pulsaciones menos de media cuando rodaban en la eléctrica. Y en ambos casos se mantuvieron en una zona de pulsaciones media-alta, justo en la zona que se fortalece la capacidad cardíaca.</p>
<br>
<img src="fondo/act1.jpg" alt="" width="20%" height="20%">

        <p style="line-height: 25px;">Sin embargo, en el estudio también pudieron confirmar que <b> montar en bici eléctrica se percibe como una actividad que no es difícil</b>, el 61% dijeron que les parecía más fácil. </p>
        <p style="line-height: 25px;">El estudio asegura que el entrenamiento realizado con bicis eléctricas fue efectivo para los ciclistas, aunque su percepción fuera la de que les supuso un menor esfuerzo.</p>
        <br>
        
        <p style="line-height: 25px;">Además de confirmar que las bicicletas eléctricas se pueden usar por cualquier ciclista aficionado para entrenar, deja la puerta abierta a valorar los beneficios que supondrá en ancianos o personas sedentarias, donde se espera que los resultados sean mucho más positivos.</p>
        <p style="line-height: 25px;">Vale la pena recordar la opinión de un ciclista tan experimentado como Purito Rodriguez, quién asegura que cuando sale a entrenar con bicleta eléctrica es cuando más cansado llega, ya que la sensación de facilidad le lleva a exigirse más y rodar hasta que se queda sin batería.</p>
