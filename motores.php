<p style="line-height: 25px;">El desarrollo de los sistemas de pedaleo asistido pasan actualmente por conseguir mayor reactividad y mayor torque. Por un lado, las motorizaciones con sensor de torque avanzan para conseguir aceleraciones sorprendentes. </p>
