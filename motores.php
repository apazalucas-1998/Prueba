<div class="margenes">
  <header class="hea1">¿Sensor de movimiento o sensor de torque? </header>
  
      <p style="line-height: 25px;">Hay dos maneras de poner en marcha el motor de una bici eléctrica: una de ellas se consigue mediante la colocación de un sensor de movimiento en el eje de pedalier. Cuando este captor detecta el giro del eje, manda la señal al controlador para que haga funcionar el motor. </p>
      <img src="fondo/sensor1.jpg" alt="" width="400px" height="300px">  
      <p style="line-height: 25px;">Pero hay otra forma mucho más  reactiva de encender el motor: mediante el uso de un sensor de torque alojado en el eje de pedalier. Este sistema está muy extendido en motores centrales.</p>
    <img src="fondo/sensor2.jpg" alt="" width="400px" height="300px">  

  <div class="cont1">
  <header class="hea456"> Motores HUB</header>
        <header class="hea2"> Motores HUB genéricos:</header>
        <p style="line-height: 25px;">Son todos aquellos integrados en los <b>bujes de las ruedas</b>, ya sean delanteras o traseras. Es el motor más extendido; los encontramos en bicicletas de gama media y en casi todos los kits de conversión.</p>
        <img src="fondo/hub1.jpg" alt="" width="400px" height="300px">  
<p style="line-height: 25px;">Alguna de las marcas incorporan en sus modelos un selector de potencia de hasta 6 niveles y algunas otras tienen la opción de instalar un acelerador. Generalmente operan a 36V, reservando los 48V a motores que superan los 500 W nominales.</p>


        <header class="hea3">Motor HUB Gocycle.</header>
        <p style="line-height: 25px;">Este pequeño motor que opera a 24V está integrado en la rueda delantera de las Gocycle. Se hace funcionar a través de un pulsador en el manillar o mediante el pedaleo.</p>
        <img src="fondo/hub2.jpg" alt="" width="400px" height="300px">
        <p style="line-height: 25px;">Sus 4 niveles de asistencia al pedaleo y su batería de gran capacidad , la hacen muy apta para cualquier tipo de uso: urbano, cross, grandes rutas o paseo.</p>
  </div>



<hr class="hr2">
<hr id="hr2">


  <div class="cont2">
      
  <header class="hea4567">  Motores Centrales. </header>

      <p style="line-height: 25px;">Este motor central irrumpió en 2013 de una forma que pocos son capaces de hacer. Sus 250W de potencia nominales operan a 36V, obteniendo una efectividad y un par de salida que puede llegar hasta los 75 Nm en continuo.</p>
      <header class="hea4">Motor BOSCH.</header>
      <p style="line-height: 25px;">Este motor central irrumpió en 2013 de una forma que pocos son capaces de hacer. Sus 250W de potencia nominales operan a 36V, obteniendo una efectividad y un par de salida que puede llegar hasta los 75 Nm en continuo.</p>
      <img src="fondo/centrales1.jpg" alt="" width="400px" height="300px">


      <header class="hea5">Motor YAMAHA.  </header>
      <p style="line-height: 25px;">Motor YAMAHA. Yamaha se metió de lleno en el mercado europeo hace varios años a través de gigantes como Haibike, Lapierre y BH Emotion. Tras el bombazo de ventas, muchos otros fabricantes lo eligieron enseguida para propulsar sus e-bikes. La idea detrás de este motor fue la de proporcionar la mayor agresividad posible, por ello se instalaron casi en exclusiva en bicicletas eléctricas de montaña.</p>
      <img src="fondo/h.jpg" alt="" width=width="400px" height="300px">
      <p style="line-height: 25px;"> Por cierto que el Yamaha PW-45 es el único motor central de alta gama que arroja 500W, solo disponibles para ebikes de alta velocidad que es necesario matricular.</p>
      <img src="fondo/h1.jpg" alt="" width="400px" height="300px">


      <header class="hea6">Motor BROSE  </header>
      <p style="line-height: 25px;">Nada menos que 90 Nm de pico te prometen los señores de Brose si compras uno de sus cacharros, pero por desgracia no disponemos de información sobre torque que ofrece este motor en continuo. Aunque la respuesta es muy progresiva y rápida, nuestra sensación es que el par es -como mucho- similar al Performance CX y al Yamaha.</p>
      <img src="fondo/brose.jpg" alt=""width="400px" height="300px">
      <p style="line-height: 25px;">Los motores Brose más agresivos y para Off-Road los encontramos en las Gamas Drive S Alu y Drive S Mag. Este último destaca por su increíble peso: menos de 3 kilos.</p>
      <img src="fondo/motor-brose-2019.jpg" alt="" width="400px" height="300px">
      <p style="line-height: 25px;">Los motores de Specialized son motores Brose «customizados» por la propia marca. Aunque el nucleo de motor es el mismo, estos motores poseen ligeras variaciones en la carcasa. Sin embargo, la electrónica de comunicación es propia de Specialized. Se comercializan en 2020 como 1.2, 1.2S y 1.3. </p>

      <header class="hea7">Motor SHIMANO STEPS  </header>
      <p style="line-height: 25px;">Existen 5 variantes de este motor según el uso que se pretende dar. El récord de peso se lo lleva la variante E6100 con sus 2,88 kgs. Enfocado a un uso urbano y de trekking, este discretísimo motor central es muy fiable y con una autonomía</p>
      <img src="fondo/city-motor-e6100-shimano-steps-en-biobike.jpg" srcset=""alt="" width="400px" height="300px">

      <header class="hea8">Motor HPR 120S – TQ Flyon  </header>
      <p style="line-height: 25px;">Este desarrollo conjunto entre las empresas TQ y Haibike generó la mayor de las expectaciones en la última edición de Eurobike.</p>
      <img srcset="fondo/flyon-en-biobike.jpg" alt=""width="400px" height="300px">
      
      <header class="hea9">Motor IMPULSE  </header>
      <p style="line-height: 25px;">Presentado en 2011 y actualmente en su versión 2.2, este motor se diseña e integra en bicicletas eléctricas de la marca Kalkhoff</p>
      <p style="line-height: 25px;">Actualmente cuenta con tres variantes: EVO, EVO Next y EVO RS. Esta última también tiene su versión High Speed que proporciona asistencia hasta los 45 km/h y 80 Nm de par de salida.</p>
      <img src="fondo/motor-kalkhoff-impulse.jpg" alt="" width="400px" height="300px" aria-sort="hola">

